function tambahAB (a,b) {
    return a+b;
}

function kurangAB (a,b) {
    return a-b;
}

function bagiAB (a,b) {
    return a/b;
}

function kaliAB (a,b) {
    return a*b;
}

function akarAB (a,b) {
    return Math.sqrt(a,b);
}

module.exports = {
    tambahAB,
    kurangAB,
    bagiAB,
    kaliAB,
    akarAB
}